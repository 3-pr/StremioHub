// ==================== Stremio API ====================
const BASE_URL = 'https://api.strem.io/api';
const CINEMETA_BASE = 'https://v3-cinemeta.strem.io';

const StremioAPI = {
  async login(email, password) {
    const res = await fetch(`${BASE_URL}/login`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ email, password, FacebookTkn: '' })
    });
    const data = await res.json();
    if (data.error) {
      const errMsg = typeof data.error === 'string' ? data.error : (data.error.message || 'Login failed');
      throw new Error(errMsg);
    }
    if (data.result?.authKey) {
      return {
        authKey: data.result.authKey,
        email: data.result.email || email,
        userId: data.result._id || data.result.id
      };
    }
    return null;
  },

  async getLibrary(authKey) {
    const res = await fetch(`${BASE_URL}/datastoreGet`, {
      method: 'POST',
      headers: { 'Content-Type': 'text/plain;charset=UTF-8' },
      body: JSON.stringify({ authKey, collection: 'libraryItem', ids: [], all: true })
    });
    const data = await res.json();
    if (data.error) throw new Error(data.error);
    return data.result || [];
  },

  async getAddons(authKey) {
    const res = await fetch(`${BASE_URL}/addonCollectionGet`, {
      method: 'POST',
      headers: { 'Content-Type': 'text/plain;charset=UTF-8' },
      body: JSON.stringify({ authKey, update: true })
    });
    const data = await res.json();
    if (data.error) throw new Error(data.error);
    return data.result?.addons || [];
  },

  async addToLibrary(authKey, itemMeta) {
    const now = new Date().toISOString();
    const imdbId = itemMeta.imdb_id || itemMeta.id || itemMeta._id;
    const type = itemMeta.type || 'movie';
    const name = itemMeta.name || '';

    const libraryItem = {
      _id: imdbId,
      id: imdbId,
      imdb_id: imdbId,
      name: name,
      type: type,
      poster: itemMeta.poster || '',
      posterShape: 'poster',
      background: itemMeta.background || '',
      logo: itemMeta.logo || '',
      description: itemMeta.description || '',
      releaseInfo: String(itemMeta.releaseInfo || itemMeta.year || ''),
      year: String(itemMeta.year || itemMeta.releaseInfo || ''),
      runtime: String(itemMeta.runtime || ''),
      imdbRating: String(itemMeta.imdbRating || ''),
      genres: Array.isArray(itemMeta.genres) ? itemMeta.genres : (Array.isArray(itemMeta.genre) ? itemMeta.genre : []),
      director: Array.isArray(itemMeta.director) ? itemMeta.director : [],
      cast: Array.isArray(itemMeta.cast) ? itemMeta.cast : [],
      released: itemMeta.released ? String(itemMeta.released) : now,
      slug: itemMeta.slug || `${type}/${name.toLowerCase().replace(/[^a-z0-9]+/g, '-')}-${imdbId}`,
      behaviorHints: {
        defaultVideoId: type === 'movie' ? imdbId : (itemMeta.behaviorHints && itemMeta.behaviorHints.defaultVideoId ? String(itemMeta.behaviorHints.defaultVideoId) : null),
        hasScheduledVideos: itemMeta.behaviorHints ? !!itemMeta.behaviorHints.hasScheduledVideos : false,
      },
      removed: false,
      temp: false,
      _ctime: itemMeta._ctime || now,
      _mtime: itemMeta._mtime || now,
      state: {
        lastWatched: now,
        timeWatched: 0,
        timeOffset: 0,
        overallTimeWatched: 0,
        timesWatched: 0,
        flaggedWatched: 0,
        duration: 0,
        video_id: type === 'movie' ? imdbId : null,
        watched: null,
        noNotif: false,
      }
    };

    if (libraryItem.type === 'movie') {
      libraryItem.videos = [];
    }

    const res = await fetch(`${BASE_URL}/datastorePut`, {
      method: 'POST',
      headers: { 'Content-Type': 'text/plain;charset=UTF-8' },
      body: JSON.stringify({ authKey, collection: 'libraryItem', changes: [libraryItem] })
    });
    const data = await res.json();
    if (data.error) {
      console.error('addToLibrary API Error:', data.error);
      throw new Error(data.error.message || 'API Error');
    }
    return true;
  },

  async markAsWatched(authKey, libraryItem) {
    const currentState = libraryItem.state || {};
    const isWatched = (currentState.timesWatched || 0) > 0;
    const newTimesWatched = isWatched ? 0 : 1;

    const payloadItem = {
      ...libraryItem,
      _mtime: new Date().toISOString(),
      state: {
        ...currentState,
        lastWatched: new Date().toISOString(),
        timesWatched: newTimesWatched,
        watched: null
      }
    };

    const res = await fetch(`${BASE_URL}/datastorePut`, {
      method: 'POST',
      headers: { 'Content-Type': 'text/plain;charset=UTF-8' },
      body: JSON.stringify({
        authKey, collection: 'libraryItem',
        changes: [payloadItem]
      })
    });
    return { ok: res.ok, isWatched: newTimesWatched > 0 };
  },

  async removeFromLibrary(authKey, libraryItem) {
    const payloadItem = {
      ...libraryItem,
      removed: true,
      removedAt: new Date().toISOString(),
      _mtime: new Date().toISOString()
    };

    if (payloadItem.releaseInfo) payloadItem.releaseInfo = String(payloadItem.releaseInfo);
    if (payloadItem.year) payloadItem.year = String(payloadItem.year);
    if (!Array.isArray(payloadItem.genres)) payloadItem.genres = [];
    if (!Array.isArray(payloadItem.cast)) payloadItem.cast = [];

    const res = await fetch(`${BASE_URL}/datastorePut`, {
      method: 'POST',
      headers: { 'Content-Type': 'text/plain;charset=UTF-8' },
      body: JSON.stringify({
        authKey, collection: 'libraryItem',
        changes: [payloadItem]
      })
    });

    const data = await res.json();
    if (data.error) throw new Error(data.error.message);
    return true;
  },

  async clearProgress(authKey, libraryItem) {
    const payloadItem = {
      ...libraryItem,
      _mtime: new Date().toISOString(),
      state: {
        ...(libraryItem.state || {}),
        timeOffset: 0,
        timeWatched: 0,
        overallTimeWatched: 0,
        video_id: null,
        lastWatched: new Date().toISOString()
      }
    };
    
    if (libraryItem.temp) {
      payloadItem.removed = true;
      payloadItem.temp = false;
    }

    const res = await fetch(`${BASE_URL}/datastorePut`, {
      method: 'POST',
      headers: { 'Content-Type': 'text/plain;charset=UTF-8' },
      body: JSON.stringify({
        authKey, collection: 'libraryItem',
        changes: [payloadItem]
      })
    });
    return res.ok;
  }
};

// ==================== Universal Addon Engine ====================
const SearchEngine = {
  async search(query) {
    const encoded = encodeURIComponent(query);
    const urlsToFetch = [];
    
    if (!state.addons || state.addons.length === 0) {
      urlsToFetch.push(`${CINEMETA_BASE}/catalog/movie/top/search=${encoded}.json`);
      urlsToFetch.push(`${CINEMETA_BASE}/catalog/series/top/search=${encoded}.json`);
    } else {
      state.addons.forEach(addon => {
        if (!addon.manifest || !addon.manifest.catalogs) return;
        addon.manifest.catalogs.forEach(cat => {
          let supportsSearch = false;
          if (cat.extra) {
            supportsSearch = cat.extra.some(e => e.name === 'search');
          } else if (cat.extraSupported) {
            supportsSearch = cat.extraSupported.includes('search');
          }
          
          if (supportsSearch && (cat.type === 'movie' || cat.type === 'series')) {
            const baseUrl = addon.transportUrl.replace('/manifest.json', '');
            urlsToFetch.push(`${baseUrl}/catalog/${cat.type}/${cat.id}/search=${encoded}.json`);
          }
        });
      });
    }

    const responses = await Promise.allSettled(urlsToFetch.map(url => fetch(url).then(r => r.json())));
    
    let allMetas = [];
    responses.forEach(res => {
      if (res.status === 'fulfilled' && res.value && res.value.metas) {
        allMetas.push(...res.value.metas);
      }
    });

    const seen = new Set();
    const uniqueMetas = [];
    allMetas.forEach(meta => {
      const id = meta.imdb_id || meta.id;
      if (!seen.has(id)) {
        seen.add(id);
        uniqueMetas.push(meta);
      }
    });

    return uniqueMetas;
  },

  async getMeta(type, imdbId) {
    const urlsToFetch = [];
    
    if (state.metaAddon && state.metaAddon !== 'auto') {
      urlsToFetch.push(`${state.metaAddon}/meta/${type}/${imdbId}.json`);
    } else if (!state.addons || state.addons.length === 0) {
      urlsToFetch.push(`${CINEMETA_BASE}/meta/${type}/${imdbId}.json`);
    } else {
      state.addons.forEach(addon => {
        if (!addon.manifest || !addon.manifest.resources) return;
        let supportsMeta = false;
        
        if (Array.isArray(addon.manifest.resources)) {
          addon.manifest.resources.forEach(res => {
            if (typeof res === 'string' && res === 'meta') {
              if (!addon.manifest.types || addon.manifest.types.includes(type)) supportsMeta = true;
            } else if (typeof res === 'object' && res.name === 'meta') {
              if (!res.types || res.types.includes(type)) supportsMeta = true;
            }
          });
        }
        
        if (supportsMeta) {
          const baseUrl = addon.transportUrl.replace('/manifest.json', '');
          urlsToFetch.push(`${baseUrl}/meta/${type}/${imdbId}.json`);
        }
      });
    }

    if (!urlsToFetch.some(url => url.includes(CINEMETA_BASE))) {
       urlsToFetch.push(`${CINEMETA_BASE}/meta/${type}/${imdbId}.json`);
    }

    const responses = await Promise.allSettled(urlsToFetch.map(url => fetch(url).then(r => r.json())));
    
    let bestMeta = null;
    
    // Find Cinemeta fallback which is the last item in urlsToFetch (or anywhere it matched)
    let cinemetaMeta = null;
    responses.forEach((res, i) => {
      if (urlsToFetch[i].includes(CINEMETA_BASE) && res.status === 'fulfilled' && res.value?.meta) {
        cinemetaMeta = res.value.meta;
      }
    });

    // If user selected an addon (or we auto-selected the first one), try to get it
    if (state.metaAddon && state.metaAddon !== 'auto') {
      const selectedRes = responses[0];
      let selectedMeta = null;
      if (selectedRes.status === 'fulfilled' && selectedRes.value?.meta) {
        selectedMeta = selectedRes.value.meta;
      }

      if (cinemetaMeta && selectedMeta) {
        bestMeta = { ...cinemetaMeta, ...selectedMeta };
        // Preserve arrays/fields if the selected addon omitted them
        if (!selectedMeta.cast || selectedMeta.cast.length === 0) bestMeta.cast = cinemetaMeta.cast;
        if (!selectedMeta.director || selectedMeta.director.length === 0) bestMeta.director = cinemetaMeta.director;
        if (!selectedMeta.description) bestMeta.description = cinemetaMeta.description;
      } else {
        bestMeta = selectedMeta || cinemetaMeta;
      }
    } else {
      bestMeta = cinemetaMeta;
    }

    if (!bestMeta) throw new Error('Meta not found across addons');
    return bestMeta;
  }
};

function isWatchedItem(item) {
  if (!item || !item.state) return false;
  const s = item.state;
  if ((s.timesWatched || 0) > 0 || (s.flaggedWatched || 0) > 0 || s.watched === true || s.watched === 1) return true;
  if (s.timeOffset && s.duration) {
    if (s.timeOffset / s.duration > 0.95) return true;
  }
  return false;
}

// ==================== Library Cache ====================
const CACHE_DURATION = 5 * 60 * 1000;

const Library = {
  async get(authKey, forceRefresh = false, sortType = 'lastwatched', filterType = 'all') {
    const cached = await chrome.storage.local.get(['library_cache']);
    const cache = cached.library_cache;
    const isValid = cache && (Date.now() - cache.lastFetch < CACHE_DURATION) && !forceRefresh;

    let movies, series, continueWatching;

    if (isValid) {
      movies = cache.movies;
      series = cache.series;
      continueWatching = cache.continue;
    } else {
      const allItems = await StremioAPI.getLibrary(authKey);

      movies = allItems.filter(i => i.type === 'movie' && !i.removed);
      series = allItems.filter(i => i.type === 'series' && !i.removed);
      continueWatching = allItems
        .filter(i => {
          if (i.removed && !i.temp) return false;
          
          if (state.dismissedNotifs && state.dismissedNotifs[i._id]) {
            if (i.type === 'series' && countNewEpisodes(i) > 0) return true;
            const lastWatched = i.state?.lastWatched ? new Date(i.state.lastWatched).getTime() : 0;
            if (state.dismissedNotifs[i._id] >= lastWatched) return false;
          }

          if (i.state) {
            const offset = i.state.timeOffset || i.state.timeWatched || 0;
            const duration = i.state.duration || 0;
            if (offset > 0 && (duration <= 0 || (offset / duration) < 0.95)) return true;
          }

          if (i.type === 'series' && countNewEpisodes(i) > 0) return true;

          return false;
        })
        .sort((a, b) => {
          const ta = (a.state && a.state.lastWatched) || a._mtime || 0;
          const tb = (b.state && b.state.lastWatched) || b._mtime || 0;
          return new Date(tb).getTime() - new Date(ta).getTime();
        });

      const isStartedOrWatched = (i) => isWatchedItem(i) || (i.state && (i.state.overallTimeWatched > 0 || i.state.timeWatched > 0 || i.state.timeOffset > 0));
      const totalWatched = allItems.filter(isStartedOrWatched).length;

      const newCache = { movies, series, continue: continueWatching, stats: { totalWatched }, lastFetch: Date.now() };
      await chrome.storage.local.set({ library_cache: newCache });
    }

    let filteredMovies = [...movies];
    let filteredSeries = [...series];

    if (filterType === 'watched') {
      filteredMovies = filteredMovies.filter(i => isWatchedItem(i));
      filteredSeries = filteredSeries.filter(i => isWatchedItem(i));
    } else if (filterType === 'notwatched') {
      filteredMovies = filteredMovies.filter(i => !isWatchedItem(i));
      filteredSeries = filteredSeries.filter(i => !isWatchedItem(i));
    }

    const sortFn = (a, b) => {
      if (sortType === 'name') return (a.name || '').localeCompare(b.name || '');
      if (sortType === 'namereverse') return (b.name || '').localeCompare(a.name || '');
      return new Date(b.state?.lastWatched || b._mtime || 0).getTime() - new Date(a.state?.lastWatched || a._mtime || 0).getTime();
    };

    filteredMovies.sort(sortFn);
    filteredSeries.sort(sortFn);

    return { movies: filteredMovies, series: filteredSeries, continue: continueWatching };
  },

  async invalidate() {
    await chrome.storage.local.remove(['library_cache']);
  }
};

