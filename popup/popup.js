// ==================== Library Loading ====================
async function loadLibrary(forceRefresh = false) {
  if (!state.auth) return;
  $('movies-grid').innerHTML = '';
  $('movies-grid').appendChild(createSkeletonCards(6));
  $('series-grid').innerHTML = '';
  $('series-grid').appendChild(createSkeletonCards(6));
  $('continue-list').innerHTML = '';
  $('continue-list').appendChild(createSkeletonList(3));

  try {
    const libraryData = await Library.get(state.auth.authKey, forceRefresh, state.librarySort, state.libraryFilter);
    state.library.movies = libraryData.movies;
    state.library.series = libraryData.series;
    state.library.continue = libraryData.continue;

    renderMovies();
    renderSeries();
    renderContinue();
    
    // Render Stats
    chrome.storage.local.get(['library_cache']).then(cached => {
      const cache = cached.library_cache;
      if (cache) {
        let wCount = 0;
        if (cache.stats && cache.stats.totalWatched !== undefined) {
          wCount = cache.stats.totalWatched;
        } else {
          const allM = cache.movies || [];
          const allS = cache.series || [];
          const isStartedOrWatched = (i) => isWatchedItem(i) || (i.state && (i.state.overallTimeWatched > 0 || i.state.timeWatched > 0 || i.state.timeOffset > 0));
          wCount = allM.filter(isStartedOrWatched).length + allS.filter(isStartedOrWatched).length;
        }
        const statWatched = $('stat-watched');
        if (statWatched) statWatched.textContent = wCount;
      }
    });

    StremioAPI.getAddons(state.auth.authKey).then(addons => {
      state.addons = addons;
      const statAddons = $('stat-addons');
      if (statAddons) statAddons.textContent = addons.length;

      const metaSelect = $('setting-meta-addon');
      if (metaSelect) {
        metaSelect.innerHTML = '';
        
        addons.forEach(addon => {
          if (!addon.manifest || !addon.manifest.resources) return;
          let supportsMeta = false;
          if (Array.isArray(addon.manifest.resources)) {
            addon.manifest.resources.forEach(res => {
              if (typeof res === 'string' && res === 'meta') {
                if (!addon.manifest.types || addon.manifest.types.includes('movie') || addon.manifest.types.includes('series')) {
                  supportsMeta = true;
                }
              } else if (typeof res === 'object' && res.name === 'meta') {
                if (!res.types || res.types.includes('movie') || res.types.includes('series')) {
                  supportsMeta = true;
                }
              }
            });
          }
          if (supportsMeta) {
            const opt = document.createElement('option');
            const baseUrl = addon.transportUrl.replace('/manifest.json', '');
            opt.value = baseUrl;
            opt.textContent = addon.manifest.name || 'Unknown Addon';
            metaSelect.appendChild(opt);
          }
        });
        if ((!state.metaAddon || state.metaAddon === 'auto') && metaSelect.options.length > 0) {
          state.metaAddon = metaSelect.options[0].value;
          chrome.storage.local.set({ metaAddon: state.metaAddon });
        }
        metaSelect.value = state.metaAddon;
      }
    }).catch(err => console.error("Failed to load addons", err));

    const allItems = [...state.library.movies, ...state.library.series, ...state.library.continue];
    fetchNotifications(allItems).then(() => {
      // Re-filter continue watching with new episode data
      if (state.auth) {
        Library.get(state.auth.authKey, true, state.librarySort, state.libraryFilter).then(d => {
          state.library.continue = d.continue;
          renderContinue(true);
        });
      }
    });

    if (forceRefresh) showToast(I18N[state.language || 'ar'].msg_updated, 'success');
  } catch (e) {
    console.error('Library load failed:', e);
    if (e.message?.includes('NotAuthorized') || e.message?.includes('401')) {
      await handleLogout();
      showToast(I18N[state.language || 'ar'].msg_session_expired, 'error');
    } else {
      showToast(I18N[state.language || 'ar'].msg_library_error, 'error');
    }
  }
}

