// ==================== Card Creators ====================
function createItemCard(item, isCW = false) {
  let progress = 0;
  if (item.state) {
    if (item.state.timeOffset > 0 && item.state.duration > 0) {
      progress = (item.state.timeOffset / item.state.duration) * 100;
    } else if (item.state.timeWatched > 0 && item.state.duration > 0) {
      progress = (item.state.timeWatched / item.state.duration) * 100;
    } else if (item.state.timeOffset > 0 || item.state.timeWatched > 0 || item.state.overallTimeWatched > 0) {
      progress = 35;
    }
  }
  progress = Math.min(Math.max(progress, 0), 100);

  let epBadge = '';
  if (item.type === 'series' && item.state?.video_id) {
    const parts = item.state.video_id.split(':');
    if (parts.length >= 3) {
      epBadge = `S${parts[1]} E${parts[2]}`;
    }
  }

  const card = document.createElement('div');
  card.className = 'item-card';
  if (isCW) card.classList.add('continue-card');
  card.dataset.id = item._id || '';

  const isWatched = isWatchedItem(item);
  let badgeHTML = isWatched ? `<div class="watched-badge"><svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"><polyline points="20 6 9 17 4 12"></polyline></svg></div>` : '';

  const hasNewEpsCount = countNewEpisodes(item);
  let dismissHTML = '';
  if (hasNewEpsCount > 0) {
    badgeHTML = `<div class="new-ep-badge">+${hasNewEpsCount}</div>`;
    dismissHTML = `<div class="dismiss-btn"><svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><line x1="18" y1="6" x2="6" y2="18"></line><line x1="6" y1="6" x2="18" y2="18"></line></svg></div>`;
  } else if (isCW && progress > 0) {
    dismissHTML = `<div class="dismiss-btn"><svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><line x1="18" y1="6" x2="6" y2="18"></line><line x1="6" y1="6" x2="18" y2="18"></line></svg></div>`;
  }

  card.innerHTML = `
    <img src="${item.poster || ''}" alt="${escapeHTML(item.name)}" loading="lazy">
    ${badgeHTML}
    ${dismissHTML}
    ${epBadge ? `<div class="continue-ep-badge">${epBadge}</div>` : ''}
    <div class="card-label pos-top">
      <div class="card-title">${escapeHTML(item.name)}</div>
      ${item.imdbRating ? `<div class="card-rating">★ ${item.imdbRating}</div>` : ''}
    </div>
    ${progress > 0 ? `
    <div class="poster-progress">
      <div class="poster-progress-fill" style="width:${progress.toFixed(1)}%"></div>
    </div>` : ''}
  `;
  card.addEventListener('click', (e) => {
    if (e.target.closest('.dismiss-btn')) {
      e.stopPropagation();
      dismissNotification(item, card);
      return;
    }
    openDetail(item);
  });
  return card;
}

