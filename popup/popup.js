// ==================== Helpers ====================
function escapeHTML(str) {
  if (!str) return '';
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

function debounce(fn, delay) {
  let timer;
  return function (...args) {
    clearTimeout(timer);
    timer = setTimeout(() => fn.apply(this, args), delay);
  };
}

// ==================== Notifications & CW ====================
function countNewEpisodes(item) {
  if (item.type !== 'series') return 0;
  if (!state.stremioNotifications) return 0;
  const total = state.stremioNotifications.items?.[item._id] || 0;
  if (total === 0) return 0;
  const dismissedAt = state.dismissedNotifs[item._id];
  if (!dismissedAt) return total;
  const videos = state.stremioNotifications.videos?.[item._id];
  if (!videos) return total;
  return videos.filter(r => r > dismissedAt).length;
}

function dismissNotification(item, card) {
  const videos = state.stremioNotifications?.videos?.[item._id];
  const latestReleased = videos && videos.length > 0 ? Math.max(...videos) : Date.now();
  state.dismissedNotifs[item._id] = latestReleased;
  chrome.storage.local.set({ dismissedNotifs: state.dismissedNotifs });
  
  if (state.auth && state.auth.authKey) {
    StremioAPI.clearProgress(state.auth.authKey, item).catch(console.error);
  }
  
  if (card) {
    card.style.opacity = '0';
    setTimeout(() => {
      if (card.parentElement) card.remove();
    }, 300);
  }
}

async function fetchNotifications(libraryItems) {
  const seriesIds = libraryItems
    .filter((i) => i.type === 'series' && !i.removed && /^tt\d+$/.test(i._id))
    .map((i) => i._id);
  if (seriesIds.length === 0) {
    state.stremioNotifications = {};
    return;
  }
  const BATCH = 50;
  const batches = [];
  for (let i = 0; i < seriesIds.length; i += BATCH) batches.push(seriesIds.slice(i, i + BATCH));
  const itemMap = {};
  libraryItems.forEach((i) => { itemMap[i._id] = i; });
  
  const results = await Promise.all(batches.map(batch => 
    fetch(`https://v3-cinemeta.strem.io/catalog/series/last-videos/lastVideosIds=${batch.join(',')}.json`).then(r => r.json()).catch(() => ({ metasDetailed: [] }))
  ));
  
  const notifications = {};
  const notificationVideos = {};
  const now = new Date();
  
  results.forEach(result => {
    (result.metasDetailed || []).forEach(meta => {
      const item = itemMap[meta.id || meta.imdb_id];
      if (!item) return;
      const lastWatched = item.state?.lastWatched ? new Date(item.state.lastWatched) : null;
      const ctime = item._ctime ? new Date(item._ctime) : null;
      const baseline = (lastWatched && ctime && lastWatched > ctime) ? lastWatched : ctime;
      if (!baseline) return;
      
      const newVideos = (meta.videos || []).filter(v => {
        if (!v.released) return false;
        const released = new Date(v.released);
        if (released > now) return false;
        if (released <= baseline) return false;
        return true;
      });
      if (newVideos.length > 0) {
        notifications[meta.id || meta.imdb_id] = newVideos.length;
        notificationVideos[meta.id || meta.imdb_id] = newVideos.map(v => new Date(v.released).getTime());
      }
    });
  });
  
  state.stremioNotifications = { items: notifications, videos: notificationVideos, fetchedAt: Date.now() };
}



