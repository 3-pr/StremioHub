async function performSearch(query) {
  $('search-grid-movies').innerHTML = '';
  $('search-grid-series').innerHTML = '';
  $('search-grid-movies').appendChild(createSkeletonCards(3));
  $('search-grid-series').appendChild(createSkeletonCards(3));
  $('search-results-movies').classList.remove('hidden');
  $('search-results-series').classList.remove('hidden');
  $('search-empty-state').classList.add('hidden');

  try {
    const results = await SearchEngine.search(query);
    state.searchResults = results;
    renderSearchResults(results);
  } catch (e) {
    console.error('Search failed:', e);
    $('search-results-movies').classList.add('hidden');
    $('search-results-series').classList.add('hidden');
    $('search-empty-state').classList.remove('hidden');
  }
}

function renderSearchResults(results) {
  const moviesGrid = $('search-grid-movies');
  const seriesGrid = $('search-grid-series');
  const moviesSection = $('search-results-movies');
  const seriesSection = $('search-results-series');
  const emptyState = $('search-empty-state');

  moviesGrid.innerHTML = '';
  seriesGrid.innerHTML = '';

  const movies = results.filter(r => r.type === 'movie');
  const series = results.filter(r => r.type === 'series');

  if (!movies.length && !series.length) {
    moviesSection.classList.add('hidden');
    seriesSection.classList.add('hidden');
    emptyState.classList.remove('hidden');
    return;
  }

  emptyState.classList.add('hidden');

  if (movies.length) {
    moviesSection.classList.remove('hidden');
    movies.forEach(item => moviesGrid.appendChild(createItemCard({
      _id: item.imdb_id || item.id, name: item.name, poster: item.poster,
      year: item.releaseInfo || item.year, type: item.type, imdbRating: item.imdbRating
    })));
  } else {
    moviesSection.classList.add('hidden');
  }

  if (series.length) {
    seriesSection.classList.remove('hidden');
    series.forEach(item => seriesGrid.appendChild(createItemCard({
      _id: item.imdb_id || item.id, name: item.name, poster: item.poster,
      year: item.releaseInfo || item.year, type: item.type, imdbRating: item.imdbRating
    })));
  } else {
    seriesSection.classList.add('hidden');
  }
}

