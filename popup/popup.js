function renderDetail(meta, libraryItem) {
  const bg = document.querySelector('.detail-bg');
  if (meta.background || meta.poster) {
    bg.style.backgroundImage = `url('${meta.background || meta.poster}')`;
  }
  $('detail-poster').src = meta.poster || libraryItem.poster || '';
  $('detail-title').textContent = meta.name || libraryItem.name || '';
  $('detail-year').textContent = meta.year || libraryItem.year || '';
  $('detail-runtime').textContent = meta.runtime || '';

  $('detail-rating').textContent = meta.imdbRating ? `⭐ ${meta.imdbRating}` : '';

  const progressEl = $('detail-watch-progress');
  if (progressEl) {
    let watchInfo = '';
    const s = libraryItem.state;
    if (s) {
      if (libraryItem.type === 'series' && s.video_id) {
        const parts = s.video_id.split(':');
        if (parts.length >= 3) {
          watchInfo += `<span class="meta-chip highlight-chip" style="border-color:var(--accent);color:var(--accent-light);">الموسم ${parts[1]} · الحلقة ${parts[2]}</span>`;
        }
      }

      const totalMin = Math.floor((s.duration || 0) / 60000);
      const watchedMin = Math.floor((s.timeOffset || s.timeWatched || 0) / 60000);

      if (totalMin > 0 && watchedMin > 0) {
        const remainingMin = totalMin - watchedMin;
        if (remainingMin > 0) {
          watchInfo += `<span class="meta-chip">متبقي ${remainingMin} دقيقة</span>`;
        } else {
          watchInfo += `<span class="meta-chip">مكتمل</span>`;
        }
      }
    }
    progressEl.innerHTML = watchInfo;
  }

  $('detail-genres').innerHTML = (meta.genres || []).map(g =>
    `<span class="genre-tag">${escapeHTML(g)}</span>`
  ).join('');

  $('detail-description').textContent = meta.description || '';

  const castEl = $('detail-cast');
  const castList = meta.cast || [];
  if (castList.length === 0) {
    castEl.innerHTML = '';
  } else if (typeof castList[0] === 'string') {
    castEl.innerHTML = castList.slice(0, 8).map((name, i) => {
      const initials = name.split(' ').map(n => n[0]).join('').substring(0, 2).toUpperCase();
      return `
      <div class="cast-item">
        <div class="actor-img-placeholder" id="actor-img-${i}">
          ${initials}
        </div>
        <span>${escapeHTML(name)}</span>
      </div>
      `;
    }).join('');

    // Fetch actor images asynchronously from TVMaze API (Free, no key needed)
    castList.slice(0, 8).forEach(async (name, i) => {
      try {
        const res = await fetch(`https://api.tvmaze.com/search/people?q=${encodeURIComponent(name)}`);
        if (!res.ok) return;
        const data = await res.json();
        const imgUrl = data?.[0]?.person?.image?.medium;
        if (imgUrl) {
          const placeholder = $(`actor-img-${i}`);
          if (placeholder) {
            placeholder.innerHTML = `<img src="${imgUrl}" alt="${escapeHTML(name)}" style="width:100%;height:100%;object-fit:cover;border-radius:50%;border:none;">`;
            placeholder.style.background = 'transparent';
          }
        }
      } catch (e) { /* ignore */ }
    });
  } else {
    castEl.innerHTML = castList.slice(0, 8).map(actor => `
      <div class="cast-item">
        <img src="${actor.photo || ''}" alt="${escapeHTML(actor.name || '')}">
        <span>${escapeHTML(actor.name || '')}</span>
      </div>
    `).join('');
  }
}

